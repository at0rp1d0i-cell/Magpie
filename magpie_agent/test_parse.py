import re
import json

text = """
根据您的需求，我为您查询到了162条符合要求的航班，最低价:490元，最短耗时:1h55m。
最低价航班为： 航班号：KN5987，起飞时间：2026-03-01 20:55:00，到达时间：2026-03-01 23:15:00，耗时：2h20m，无需中转，超值经济舱价格：490元
最短耗时航班为： 航班号：CZ1953，起飞时间：2026-03-01 20:55:00，到达时间：2026-03-01 22:50:00，耗时：1h55m，无需中转，经济舱价格：980元
除了上述方案外还为您推荐以下方案：
1. 航班号：MF8561，起飞时间：2026-03-01 07:55:00，到达时间：2026-03-01 10:25:00，耗时：2h30m，无需中转，经济舱价格：490元
"""

pattern = re.compile(r"航班号：([A-Z0-9]+)，起飞时间：([\d\-: ]+)，到达时间：([\d\-: ]+)，耗时：([^，]+)，.*?价格：(\d+)元")
matches = pattern.findall(text)

inventory = []
for m in matches:
    inventory.append({
        "vehicle_code": m[0],
        "start_time": m[1][11:16],
        "arrive_time": m[2][11:16],
        "duration": m[3],
        "price_info": f"￥{m[4]}",
    })

print(json.dumps(inventory, indent=2))
